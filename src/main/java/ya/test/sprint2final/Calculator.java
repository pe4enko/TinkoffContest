    public static Integer calculate(String[] expression) {
        final LinkedList<Integer> operands = new LinkedList<>();

        for (String elem : expression) {
            IntBinaryOperator operator = parseOperation(elem);

            if (operator != null) {
                Integer rightOperand = operands.pop();
                Integer leftOperand = operands.pop();

                operands.push(operator.applyAsInt(leftOperand, rightOperand));
            } else {
                operands.push(Integer.parseInt(elem));
            }
        }
        return operands.pop();
    }

    private static IntBinaryOperator parseOperation(String s) {
        if ("+".equals(s)) {
            return Math::addExact;
        }
        if ("-".equals(s)) {
            return Math::subtractExact;
        }
        if ("/".equals(s)) {
            return Math::floorDiv;
        }
        if ("*".equals(s)) {
            return Math::multiplyExact;
        }
        return null;
    }

